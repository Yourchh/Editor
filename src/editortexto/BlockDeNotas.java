    public void imprimirTextoPlano(JTextArea txtaContenido) {
        // Validar si el JTextArea tiene texto
        if (txtaContenido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay texto que imprimir.");
            return;
        }

        // Obtener el texto del JTextArea
        String texto = txtaContenido.getText();

        // Mostrar un diálogo de progreso
        JDialog dialogoProgreso = new JDialog(this, "Imprimiendo...");
        dialogoProgreso.setVisible(true);

        // Crear un botón de cancelación
        JButton btnCancelar = new JButton("Cancelar");
        btnCancelar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                printJob.cancel();
                dialogoProgreso.setVisible(false);
            }
        });

        // Agregar el botón al diálogo de progreso
        dialogoProgreso.add(btnCancelar, BorderLayout.SOUTH);

        // Crear un objeto PrintJob para la impresión
        PrintJob printJob = Toolkit.getDefaultToolkit().getPrintJob(this, "Imprimir", null);

        // Validar si el usuario seleccionó una impresora
        if (printJob == null) {
            JOptionPane.showMessageDialog(this, "No se seleccionó una impresora.");
            dialogoProgreso.setVisible(false);
            return;
        }

        // Obtener el objeto Graphics del PrintJob
        Graphics g = printJob.getGraphics();

        // Imprimir el texto
        try {
            g.drawString(texto, 10, 10);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al imprimir: " + e.getMessage());
        }

        // Finalizar la impresión
        g.dispose();
        printJob.end();

        // Ocultar el diálogo de progreso
        dialogoProgreso.setVisible(false);
    }

