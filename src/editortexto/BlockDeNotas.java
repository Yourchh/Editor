    public int obtenerNumeroPalabras(JTextArea df) {
        String[] palabras = df.getText().split("\\s+");
        return palabras.length;
    }

    public int obtenerNumeroCaracteres(JTextArea df) {
        return df.getText().length();
    }

    public int obtenerNumeroLinea(int posicionCursor) {
        int numeroLinea = 1;
        for (int i = 0; i < posicionCursor; i++) {
            if (txtaContenido.getText().charAt(i) == '\n') {
                numeroLinea++;
            }
        }
        return numeroLinea;
    }

    public int obtenerColumna(int posicionCursor) {
        int columna = 1;
        for (int i = 0; i < posicionCursor; i++) {
            if (txtaContenido.getText().charAt(i) == '\n') {
                columna = 1;
            } else {
                columna++;
            }
        }
        return columna;
    }

    public String obtenerIdiomaTeclado() {
        Locale locale = Locale.getDefault();
        return locale.getLanguage();
    }

    public static String obtenerFecha() {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        return sdf.format(new Date());
    }

